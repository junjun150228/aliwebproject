package com.ali.entity;

/**
 * 按天统计信息
 */
public class DateSumInfo {

    private String day;

    private int count;

    private int type;

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }
}
